#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new;

	new = malloc(sizeof(t_list));
	if (!new)
		return (0);
	new->content = NULL;
	new->next = NULL;
	if (content)
		new->content = content;
	return (new);
}
