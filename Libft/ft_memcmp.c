#include "libft.h"

int			memcmp(const void *s1, const void *s2, unsigned int n)
{
	char	*p1;
	char	*p2;

	p1 = (char*)s1;
	p2 = (char*)s2;
	while (n--)
	{
		if (*p1 != *p2)
			return (1);
		p1++;
		p2++;
	}
	return (0);
}
