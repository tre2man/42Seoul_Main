#include <stdio.h>

int main()
{
    /* %[플래그][폭][정밀도][길이]타입 */
    int number = 12345;
    char c = 'A';
    char *str = "abcdef";

    /* d */
    /* 폭 */
    printf("[%2d]\n", 5);		//	[ 5]
    printf("[%2d]\n", 123);		//	[123]

    /* [-][width] */
    printf("[%-2d]\n", 5);		//	[5 ]
    printf("[%-2d]\n", 123);		//	[123]

    /* [0][width] */
    printf("[%02d]\n", 5);		//	[05]
    printf("[%02d]\n", 123);		//	[123]

    /* [*] 음수 */
    printf("[%*d]\n", -2, 5);	//	[5 ]
    printf("[%*d]\n", -2, 123);	//	[123]

    /* [-][*] 음수 */
    printf("[%-*d]\n", -2, 5);	//	[5 ]
    printf("[%-*d]\n", -2, 123);	//	[123]

    /* [0][*] 음수 */    
    printf("[%0*d]\n", -2, 5);	//	[5 ]
    printf("[%0*d]\n", -2, 123);	//	[123]


    /* d 정밀도 */
    /* [][.N] */
    printf("[%.2d]\n", 5);		//	[05] 
    printf("[%.2d]\n", 123);		//	[123]

    /* [-][.N] */
    printf("[%-.2d]\n", 5);		//	[05] 
    printf("[%-.2d]\n", 123);		//	[123]

    /* [0][.N] */
    printf("[%0.2d]\n", 5);		//	[05]
    printf("[%0.2d]\n", 123);		//	[123]

    /* [width][.N] */
    printf("[%10.2d]\n", 5);		//	[        05]
    printf("[%5.2d]\n", 123);		//	[  123]

    /* [0][width][.N] */
    printf("[%010.2d]\n", 5);		//	[        05]
    printf("[%05.2d]\n", 123);	//	[  123]

    /* [-][width][.N] */
    printf("[%-10.2d]\n", 5);		//	[05        ]
    printf("[%-5.2d]\n", 123);	//	[123  ]

    /* [][.0] */
    printf("[%.0d]\n", 5);		//	[5] 
    printf("[%.0d]\n", 123);		//	[123]

    /* [-][.0] */
    printf("[%-.0d]\n", 5);		//	[5] 
    printf("[%-.0d]\n", 123);		//	[123]

    /* [0][.0] */
    printf("[%0.0d]\n", 5);		//	[5]
    printf("[%0.0d]\n", 123);		//	[123]

    /* [width][.0] */
    printf("[%10.0d]\n", 5);		//	[         5]
    printf("[%5.0d]\n", 123);		//	[  123]

    /* [0][width][.0] */
    printf("[%010.0d]\n", 5);		//	[         5]
    printf("[%05.0d]\n", 123);	//	[  123]

    /* [-][width][.0] */
    printf("[%-10.0d]\n", 5);		//	[5         ]
    printf("[%-5.0d]\n", 123);	//	[123  ]

    /* [][.*] 음수 */
    printf("[%.*d]\n", -2, 5);		//	[5] 
    printf("[%.*d]\n", -2, 123);	//	[123]

    /* [-][.*] 음수 */
    printf("[%-.*d]\n", -2, 5);		//	[5] 
    printf("[%-.*d]\n", -2, 123);	//	[123]

    /* [0][.*] 음수 */
    printf("[%0.*d]\n", -2, 5);		//	[5]
    printf("[%0.*d]\n", -2, 123);	//	[123]

    /* [width][.*] 음수 */
    printf("[%10.*d]\n", -2, 5);	//	[         5]
    printf("[%5.*d]\n", -2, 123);	//	[  123]

    /* [0][width][.*] 음수 */
    printf("[%010.*d]\n", -2, 5);	//	[0000000005]
    printf("[%05.*d]\n", -2, 123);	//	[00123]

    /* [-][width][.*] 음수 */
    printf("[%-10.*d]\n", -2, 5);	//	[5         ]
    printf("[%-5.*d]\n\n", -2, 123);	//	[123  ]


    /* s */
    /* [width] */
    printf("[%2s]\n", "a");		//	[ a]
    printf("[%2s]\n" "abc");		//	[abc]

    /* [-][width] */
    printf("[%-2s]\n", "a");		//	[a ]
    printf("[%-2s]\n", "abc");	//	[abc]

    /* [*] 음수 */
    printf("[%*s]\n", -2, "a");	//	[a ]
    printf("[%*s]\n", -2, "abc");	//	[abc]

    /* [-][*] 음수 */
    printf("[%-*s]\n", -2, "a");	//	[a ]
    printf("[%-*s]\n", -2, "abc");	//	[abc]

    
    /* s 정밀도 */
    /* [][.N] */
    printf("[%.2s]\n", "a");		//	[a] 
    printf("[%.2s]\n", "abc");	//	[ab]

    /* [-][.N] */
    printf("[%-.2s]\n", "a");		//	[a] 
    printf("[%-.2s]\n", "abc");	//	[ab]

    /* [width][.N] */
    printf("[%10.2s]\n", "a");	//	[         a]
    printf("[%5.2s]\n", "abc");	//	[   ab]


    /* [-][width][.N] */
    printf("[%-10.2s]\n", "a");	//	[a         ]
    printf("[%-5.2s]\n", "abc");	//	[ab   ]

    /* [][.0] */
    printf("[%.0s]\n", "a");		//	[] 
    printf("[%.0s]\n", "abc");	//	[]

    /* [-][.0] */
    printf("[%-.0s]\n", "a");		//	[] 
    printf("[%-.0s]\n", "abc");	//	[]

    /* [width][.0] */
    printf("[%10.0s]\n", "a");	//	[          ]
    printf("[%5.0s]\n", "abc");	//	[     ]

    /* [-][width][.0] */
    printf("[%-10.0s]\n", "a");	//	[          ]
    printf("[%-5.0s]\n", "abc");	//	[     ]

    /* [][.*] 음수 */
    printf("[%.*s]\n", -2, "a");	//	[a] 
    printf("[%.*s]\n", -2, "abc");	//	[abc]

    /* [-][.*] 음수 */
    printf("[%-.*s]\n", -2, "a");	//	[a] 
    printf("[%-.*s]\n", -2, "abc");	//	[abc]

    /* [width][.*] 음수 */
    printf("[%10.*s]\n", -2, "a");	//	[         a]
    printf("[%5.*s]\n", -2, "abc");	//	[  abc]


    /* [-][width][.*] 음수 */
    printf("[%-10.*s]\n", -2, "a");	//	[a         ]
    printf("[%-5.*s]\n\n", -2, "abc");	//	[abc  ]


    /* c */
    /* [width] */
    printf("[%2c]\n", 'a');		//	[ a]

    /* [-][width] */
    printf("[%-2c]\n", 'a');		//	[a ]

    /* [*] 음수 */
    printf("[%*c]\n", -2, 'a');	//	[a ]

    /* [-][*] 음수 */
    printf("[%-*c]\n\n", -2, 'a');	//	[a ]


    /* d - 0123 */
    /* [][.*] 0 */ 
    printf("[%.*d]\n", 0, 0);	//	[] 
    printf("[%.*d]\n", 0, 0123);	//	[83] 

    /* [-][.*] 0 */ 
    printf("[%-.*d]\n", 0, 0);	//	[] 
    printf("[%-.*d]\n", 0, 0123);	//	[83] 

    /* [0][.*] 0 */ 
    printf("[%0.*d]\n", 0, 0);	//	[] 
    printf("[%0.*d]\n", 0, 0123);	//	[83] 

    /* [width][.*] 0 */ 
    printf("[%10.*d]\n", 0, 0);	//	[ ] 
    printf("[%5.*d]\n", 0, 0123);	//	[ 83] 

    /* [0][width][.*] 0 */ 
    printf("[%010.*d]\n", 0, 0);	//	[ ] 
    printf("[%05.*d]\n", 0, 0123);	//	[ 83] 

    /* [-][width][.*] 0 */
    printf("[%-10.*d]\n", 0, 0);	//	[ ] 
    printf("[%-5.*d]\n", 0, 0123);	//	[83 ]
}